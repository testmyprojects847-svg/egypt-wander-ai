export function getNationalityByCode(code: string, language: 'en' | 'ar'): string | undefined {
  const nationality = nationalities.find(n => n.code === code);
  return nationality ? nationality[language] : undefined;
}

export function searchNationalities(query: string, language: 'en' | 'ar'): Nationality[] {
  const lowerQuery = query.toLowerCase();
  return nationalities.filter(n => 
    n[language].toLowerCase().includes(lowerQuery) ||
    n.code.toLowerCase().includes(lowerQuery)
  );
}
